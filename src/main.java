    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        if ("".equals(tfnis.getText()) || "".equals(tfnama.getText()) || "".equals(tfkelas.getText()) || "".equals(taalamat.getText())) {
            JOptionPane.showMessageDialog(this, "Data Anda Kurang Lengkap", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String jk = "";
            if (rdlk.isSelected()) {
                jk = "L";
            }else{
                jk = "P";
            }
            String SQL = "UPDATE t_siswa SET" 
                    + "WHERE NamaSiswa'"+tfnama.getText()+"'," 
                    + "WHERE JenisKelamin'"+jk+"'," 
                    + "WHERE Kelas'"+tfkelas.getText()+"'," 
                    + "WHERE Email'"+tfemail.getText()+"'" 
                    + "WHERE Alamat'"+taalamat.getText()+"'" 
                    + "WHERE NIS'"+tfnis.getText()+"'" ;
            int status = koneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data Berhasil diupdate", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data Gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_editActionPerformed

    private void maintableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_maintableMouseClicked
        // TODO add your handling code here:
        int baris = maintable.getSelectedRow();
        if (baris != -1) {
            tfnis.setText(maintable.getValueAt(baris, 0).toString());
            tfnama.setText(maintable.getValueAt(baris, 1).toString());
            if("Laki - Laki".equals(maintable.getValueAt(baris, 2).toString())){
            rdlk.setSelected(true);
            }else {
            rdpr.setSelected(true);
            }
            tfkelas.setText(maintable.getValueAt(baris, 3).toString());
            tfemail.setText(maintable.getValueAt(baris, 4).toString());
            taalamat.setText(maintable.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_maintableMouseClicked

